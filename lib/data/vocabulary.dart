/// 获取指定级别的词汇
List<VocabularyEntry> getVocabularyByLevel(LanguageLevel level) {
  switch (level) {
    case LanguageLevel.A1:
      return vocabularyA1;
    case LanguageLevel.A2:
      return [...vocabularyA1, ...vocabularyA2];
    case LanguageLevel.B1:
      return [...vocabularyA1, ...vocabularyA2, ...vocabularyB1];
    case LanguageLevel.B2:
      return [...vocabularyA1, ...vocabularyA2, ...vocabularyB1, ...vocabularyB2];
    default:
      return vocabularyA1;
  }
}

/// 获取随机词汇
VocabularyEntry getRandomVocabulary(LanguageLevel level) {
  final vocabList = getVocabularyByLevel(level);
  return vocabList[DateTime.now().millisecond % vocabList.length];
}

/// 统计词汇总数
int getTotalVocabularyCount() {
  return vocabularyA1.length +
         vocabularyA2.length +
         vocabularyB1.length +
         vocabularyB2.length;
}
