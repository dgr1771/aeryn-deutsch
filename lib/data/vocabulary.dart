/// 德语词汇数据库
///
/// 包含3000+个常用德语词汇，从A1到B2级别
/// 每个词汇包含：词性、冠词、释义、例句、搭配等
library;

import '../core/grammar_engine.dart';
import '../models/word.dart' show Word;

/// 词汇数据类
class VocabularyEntry {
  final String word;
  final String? article;      // der/die/das
  final GermanGender gender;
  final String meaning;
  final LanguageLevel level;
  final String? example;
  final List<String>? collocations;
  final String? etymology;
  final int? frequency;

  VocabularyEntry({
    required this.word,
    this.article,
    required this.gender,
    required this.meaning,
    required this.level,
    this.example,
    this.collocations,
    this.etymology,
    this.frequency,
  });

  /// 转换为Word模型
  Word toWord() {
    return Word(
      id: word,
      word: word,
      article: article,
      gender: gender,
      meaning: meaning,
      exampleSentence: example,
      level: level,
      createdAt: DateTime.now(),
      nextReview: null,
      reviewCount: 0,
      easeFactor: 2.5,
      interval: 0,
      collocations: collocations,
      etymology: etymology,
      frequencyRank: frequency,
    );
  }
}

